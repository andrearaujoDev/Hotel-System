    public void readTb_Lista(){
        DefaultTableModel modelo = (DefaultTableModel)tb_lista.getModel();
        modelo.setNumRows(0);
        Ctr_Quartos controle = new Ctr_Quartos();   
     
        for(Quartos q: controle.selecionarTodos()){
            modelo.addRow(new Object[]{      
            q.getIdquartos(),
            q.getNumero(),
            q.getAndar(),
            q.getDescricao(),
            q.getCaracteristicas(),
            q.getValorDiaria(),
            q.getEstado(),
            q.getTipoQuarto()});
        }
    }
    
    public void buscaFeita(String busca){
        DefaultTableModel modelo = (DefaultTableModel)tb_lista.getModel();
        modelo.setNumRows(0);
        Ctr_Quartos controle = new Ctr_Quartos();  
        
        for(Quartos q: controle.buscar(busca)){
            modelo.addRow(new Object[]{      
            q.getIdquartos(),
            q.getNumero(),
            q.getAndar(),
            q.getDescricao(),
            q.getCaracteristicas(),
            q.getValorDiaria(),
            q.getEstado(),
            q.getTipoQuarto()});
        }
    }

