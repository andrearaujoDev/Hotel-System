    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Quartos quartos = new Quartos();
        Ctr_Quartos ctrQuartos = new Ctr_Quartos();

        String numero = txt_numero.getText();
        String andar = jcb_andar.getSelectedItem().toString();
        String descricao = jta_descricao.getText();
        String caracteristicas = jta_caracteristicas.getText();
        float valor_diaria = Float.parseFloat(txt_valorDiaria.getText());
        String estado = jcb_estado.getSelectedItem().toString();
        String tipo_quarto = jcb_tipoQuarto.getSelectedItem().toString();
        int id_quarto =  (int) tb_lista.getValueAt(tb_lista.getSelectedRow(),0); 
        quartos = new Quartos(numero,andar,descricao,caracteristicas,valor_diaria,estado,tipo_quarto);
        ctrQuartos.editar(quartos,id_quarto);
        readTb_Lista();
        LimparCampos();
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

