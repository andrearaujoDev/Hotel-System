    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Pagamentos pagamento = new Pagamentos();
        Ctr_Pagamentos ctrPagamentos = new Ctr_Pagamentos();
        
        String cliente = txt_cliente.getText();
        String quarto = txt_quarto.getText();
        int diasHospedado = Integer.parseInt(txt_dias_hospedado.getText());
        double diaria = Double.parseDouble(txt_diaria.getText());
        double totalCobrado = (diasHospedado  * diaria);

        pagamento = new Pagamentos(cliente,quarto,diasHospedado,totalCobrado,diaria);
        ctrPagamentos.inserir(pagamento);
        readTb_ListaPagamentos();
        limparCampos();
        
    }//GEN-LAST:event_btnSalvarActionPerformed

