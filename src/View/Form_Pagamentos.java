    public void readTb_ListaClientes(){
        DefaultTableModel modelo = (DefaultTableModel)tb_listaClientes.getModel();
        modelo.setNumRows(0);
        Ctr_Clientes controle = new Ctr_Clientes();   
     
        for(Clientes c: controle.selecionarTodos()){
            modelo.addRow(new Object[]{      
            c.getId_cliente(),
            c.getNome(),
            c.getCpf(),
            c.getTelefone(),
            c.getEmail(),
            c.getRua(),
            c.getNumero_casa(),
            c.getBairro(),
            });
        }
    }
    
    public void readTb_ListaQuartos(){
        DefaultTableModel modelo = (DefaultTableModel)tb_listaQuartos.getModel();
        modelo.setNumRows(0);
        Ctr_Quartos controle = new Ctr_Quartos();   
     
        for(Quartos q: controle.selecionarTodos()){
            modelo.addRow(new Object[]{      
            q.getIdquartos(),
            q.getNumero(),
            q.getAndar(),
            q.getDescricao(),
            q.getCaracteristicas(),
            q.getValorDiaria(),
            q.getEstado(),
            q.getTipoQuarto()});
        }
    }
    
    public void readTb_ListaPagamentos(){
        DefaultTableModel modelo = (DefaultTableModel)tb_listaPagamentos.getModel();
        modelo.setNumRows(0);
        Ctr_Pagamentos controle = new Ctr_Pagamentos();   
     
        for(Pagamentos p: controle.selecionarTodos()){
            modelo.addRow(new Object[]{      
            p.getId_pagamento(),
            p.getCliente(),
            p.getQuarto(),
            p.getDias_hospedado(),
            p.getTotal_cobrado(),
            });
        }
    }
    
    public void buscaFeitaPagamentos(String busca){
        DefaultTableModel modelo = (DefaultTableModel)tb_listaPagamentos.getModel();
        modelo.setNumRows(0);
        Ctr_Pagamentos controle = new Ctr_Pagamentos();   
     
        for(Pagamentos p: controle.buscar(busca)){
            modelo.addRow(new Object[]{      
            p.getId_pagamento(),
            p.getCliente(),
            p.getQuarto(),
            p.getDias_hospedado(),
            p.getTotal_cobrado(),
            });
        }
    }

